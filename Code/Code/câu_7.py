import numpy as np
from pymoo.core.problem import ElementwiseProblem
from pymoo.algorithms.moo.nsga2 import NSGA2
from pymoo.optimize import minimize
import matplotlib.pyplot as plt

class VietnamDigitalProblem(ElementwiseProblem):
    def __init__(self):
        super().__init__(n_var=24, n_obj=4, n_ieq_constr=14, xl=np.zeros(24), xu=np.ones(24) * 12000)
        self.beta = np.array([
            [1.15, 0.85, 0.55, 1.30], [0.95, 1.25, 1.40, 1.05], [1.05, 0.95, 0.85, 1.15],
            [1.20, 0.75, 0.45, 1.35], [0.90, 1.30, 1.55, 1.00], [1.10, 0.85, 0.65, 1.25]
        ])
        self.e = np.array([0.42, 0.55, 0.48, 0.32, 0.62, 0.38])
        self.rho = np.array([0.18, 0.45, 0.28, 0.12, 0.52, 0.22])
        self.sig = np.array([0.32, 0.28, 0.30, 0.35, 0.25, 0.30])

    def _evaluate(self, x, out, *args, **kwargs):
        X = x.reshape(6, 4)
        f1 = -(self.beta * X).sum()
        region_sums = X.sum(axis=1)
        f2 = np.abs(region_sums - region_sums.mean()).mean()
        f3 = (self.e * (X[:, 0] + X[:, 2])).sum()
        f4 = (self.rho * X[:, 2]).sum() - (self.sig * X[:, 3]).sum()
        out["F"] = [f1, f2, f3, f4]
        g1 = X.sum() - 50000
        g2_7 = 5000 - region_sums
        g8_13 = region_sums - 12000
        g14 = 12000 - X[:, 3].sum()
        out["G"] = [g1] + g2_7.tolist() + g8_13.tolist() + [g14]

problem = VietnamDigitalProblem()
algorithm = NSGA2(pop_size=100)
res = minimize(problem, algorithm, ('n_gen', 200), seed=42, verbose=False)

