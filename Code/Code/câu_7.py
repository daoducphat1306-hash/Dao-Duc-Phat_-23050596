import numpy as np
import pandas as pd

# ---------------------------------------------------------
# BƯỚC 0: CHUẨN BỊ MA TRẬN QUYẾT ĐỊNH
# ---------------------------------------------------------
# Trích xuất dữ liệu từ tập Pareto
f1_positive = -res.F[:, 0]  # Đưa GDP gain về giá trị dương thực tế
f2 = res.F[:, 1]
f3 = res.F[:, 2]
f4 = res.F[:, 3]

# Ghép thành ma trận quyết định X (mỗi hàng là 1 phương án, 4 cột là 4 mục tiêu)
X = np.column_stack((f1_positive, f2, f3, f4))

# Cài đặt trọng số và loại tiêu chí
weights = np.array([0.40, 0.25, 0.20, 0.15])
# True = Tiêu chí Lợi ích (Max), False = Tiêu chí Chi phí (Min)
is_benefit = np.array([True, False, False, False])

# ---------------------------------------------------------
# BƯỚC 1 & 2: CHUẨN HÓA VECTOR VÀ NHÂN TRỌNG SỐ
# ---------------------------------------------------------
# Chuẩn hóa vector theo cột
R = X / np.sqrt((X**2).sum(axis=0))

# Xây dựng ma trận chuẩn hóa có trọng số
V = R * weights

# ---------------------------------------------------------
# BƯỚC 3: XÁC ĐỊNH LỜI GIẢI LÝ TƯỞNG TỐT (A*) VÀ XẤU (A-)
# ---------------------------------------------------------
A_star = np.where(is_benefit, V.max(axis=0), V.min(axis=0))
A_neg = np.where(is_benefit, V.min(axis=0), V.max(axis=0))

# ---------------------------------------------------------
# BƯỚC 4: TÍNH KHOẢNG CÁCH EUCLIDE
# ---------------------------------------------------------
S_star = np.sqrt(((V - A_star)**2).sum(axis=1))
S_neg = np.sqrt(((V - A_neg)**2).sum(axis=1))

# ---------------------------------------------------------
# BƯỚC 5: TÍNH HỆ SỐ GẦN GŨI (C*) VÀ CHỌN NGHIỆM
# ---------------------------------------------------------
C_star = S_neg / (S_star + S_neg)

# Lấy ra chỉ số (index) của phương án có C* cao nhất
best_idx = np.argmax(C_star)

# ---------------------------------------------------------
# IN KẾT QUẢ NGHIỆM THỎA HIỆP
# ---------------------------------------------------------
print("=== NGHIỆM THỎA HIỆP TỐT NHẤT THEO TOPSIS ===")
print(f"Điểm TOPSIS (C*): {C_star[best_idx]:.4f}\n")

print("1. Đánh giá trên các mục tiêu chiến lược:")
print(f"   - Tăng trưởng (GDP Gain): {X[best_idx, 0]:,.2f} tỷ VND")
print(f"   - Bất bình đẳng (Gini):   {X[best_idx, 1]:.4f}")
print(f"   - Phát thải (CO2):        {X[best_idx, 2]:,.2f}")
print(f"   - Rủi ro an ninh ròng:    {X[best_idx, 3]:,.2f}\n")

