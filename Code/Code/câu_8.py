import cvxpy as cp
import numpy as np
import matplotlib.pyplot as plt

# 1. Khởi tạo tham số
T = 10
rho = 0.97
alpha, gamma, delta, theta, beta = 0.33, 0.10, 0.08, 0.07, 0.42

# Điều kiện ban đầu năm 2026
K0 = 27500.0
L_val = 54.0
D0 = 20.3
AI0 = 86.0
H0 = 30.0
Y0 = 12847.6

# Tính A0
A0 = Y0 / ( (K0**alpha) * (L_val**beta) * (D0**gamma) * (AI0**delta) * (H0**theta) )

# 2. Khai báo biến quyết định
K  = cp.Variable(T+1, nonneg=True)
D  = cp.Variable(T+1, nonneg=True)
AI = cp.Variable(T+1, nonneg=True)
H  = cp.Variable(T+1, nonneg=True)

I_K  = cp.Variable(T, nonneg=True)
I_D  = cp.Variable(T, nonneg=True)
I_AI = cp.Variable(T, nonneg=True)
I_H  = cp.Variable(T, nonneg=True)

C = cp.Variable(T, nonneg=True)
Y = cp.Variable(T, nonneg=True)

# 3. Xây dựng ràng buộc
constraints = [
    K[0] == K0,
    D[0] == D0,
    AI[0] == AI0,
    H[0] == H0,
    C >= 1.0
]

# Trọng số cho geo_mean
weights = np.array([alpha, gamma, delta, theta, beta])

for t in range(T):
    inputs = cp.hstack([K[t], D[t], AI[t], H[t], L_val])
    constraints += [
        Y[t] <= A0 * cp.geo_mean(inputs, p=weights)
    ]

    constraints += [
        C[t] + I_K[t] + I_D[t] + I_AI[t] + I_H[t] <= Y[t]
    ]

    constraints += [
        K[t+1] == (1 - 0.05) * K[t] + I_K[t],
        D[t+1] == (1 - 0.12) * D[t] + I_D[t],
        AI[t+1] == (1 - 0.15) * AI[t] + I_AI[t],
        H[t+1] == (1 - 0.02) * H[t] + 0.8 * I_H[t]
    ]

# 4. Hàm mục tiêu
objective = cp.Maximize(cp.sum([ (rho**t) * cp.log(C[t]) for t in range(T) ]))

# 5. Giải bài toán
# Sử dụng CLARABEL làm solver thay thế cho ECOS/SCS
