import cvxpy as cp
import numpy as np
import matplotlib.pyplot as plt

def solve_investment_strategy(strategy_type="even"):
    # 1. Khởi tạo tham số cơ bản
    T = 10
    rho = 0.97
    alpha, gamma, delta, theta, beta = 0.33, 0.10, 0.08, 0.07, 0.42

    K0, L_val, D0, AI0, H0 = 27500.0, 54.0, 20.3, 86.0, 30.0
    Y0 = 12847.6
    A0 = Y0 / ( (K0**alpha) * (L_val**beta) * (D0**gamma) * (AI0**delta) * (H0**theta) )

    # 2. Khai báo biến quyết định
    K, D, AI, H = [cp.Variable(T+1, nonneg=True) for _ in range(4)]
    I_K, I_D, I_AI, I_H = [cp.Variable(T, nonneg=True) for _ in range(4)]
    C, Y = [cp.Variable(T, nonneg=True) for _ in range(2)]

    constraints = [K[0] == K0, D[0] == D0, AI[0] == AI0, H[0] == H0, C >= 1.0]
    weights = np.array([alpha, gamma, delta, theta, beta])

    # 3. Ràng buộc hệ thống cơ bản
    for t in range(T):
        inputs = cp.hstack([K[t], D[t], AI[t], H[t], L_val])
        constraints += [
            Y[t] <= A0 * cp.geo_mean(inputs, p=weights),
            C[t] + I_K[t] + I_D[t] + I_AI[t] + I_H[t] <= Y[t],
            K[t+1] == (1 - 0.05) * K[t] + I_K[t],
            D[t+1] == (1 - 0.12) * D[t] + I_D[t],
            AI[t+1] == (1 - 0.15) * AI[t] + I_AI[t],
            H[t+1] == (1 - 0.02) * H[t] + 0.8 * I_H[t]
        ]

    # 4. Ràng buộc định hình chiến lược
    if strategy_type == "even":
        for t in range(T-1):
            constraints += [I_K[t] == I_K[t+1], I_D[t] == I_D[t+1], I_AI[t] == I_AI[t+1], I_H[t] == I_H[t+1]]

    elif strategy_type == "front_load":
        for t in range(2):
            constraints += [I_K[t] == I_K[t+1], I_D[t] == I_D[t+1], I_AI[t] == I_AI[t+1], I_H[t] == I_H[t+1]]
        for t in range(3, T-1):
            constraints += [I_K[t] == I_K[t+1], I_D[t] == I_D[t+1], I_AI[t] == I_AI[t+1], I_H[t] == I_H[t+1]]
        constraints += [
            I_K[2] >= 1.5 * I_K[3], I_D[2] >= 1.5 * I_D[3],
            I_AI[2] >= 1.5 * I_AI[3], I_H[2] >= 1.5 * I_H[3]
        ]

    objective = cp.Maximize(cp.sum([ (rho**t) * cp.log(C[t]) for t in range(T) ]))
    prob = cp.Problem(objective, constraints)
    prob.solve(solver=cp.CLARABEL)

    return prob.value, (I_K.value + I_D.value + I_AI.value + I_H.value), C.value

# --- CHẠY VÀ SO SÁNH ---
welfare_even, inv_even, c_even = solve_investment_strategy("even")
welfare_front, inv_front, c_front = solve_investment_strategy("front_load")

print(f"Welfare Even: {welfare_even:.4f}")
print(f"Welfare Front-load: {welfare_front:.4f}")

years = range(2026, 2036)
